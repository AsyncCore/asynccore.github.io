DELIMITER //
DROP PROCEDURE IF EXISTS SP_CREAR_POST_EN_HILO;
CREATE PROCEDURE SP_CREAR_POST_EN_HILO(
    IN P_ID_HILO INT,
    IN P_USER_ID INT,
    IN P_CONTENIDO TEXT
)
BEGIN
    INSERT INTO POSTS (ID_HILO, USER_ID, CONTENIDO, FECHA_CREACION)
    VALUES (P_ID_HILO, P_USER_ID, P_CONTENIDO, NOW());
END;
DELIMITER ;